    public static boolean DisplayColorLetters(char[] inputWord, char[] correctWord) {
        boolean correct = true;
        char[] answerTemp = Arrays.copyOf(correctWord, getGameIndicator());;
        int[] colorForLetter = new int[getGameIndicator()]; //0 is grey, yellow is 1, green is 2

        for (int i = 0; i < getGameIndicator(); i++) { //check for any correct position+letter (green)
            if (inputWord[i] == answerTemp[i]) {
                answerTemp[i] = '-';
                colorForLetter[i] = 2;
            } else {
                correct = false;
            }
        }

        for (int j = 0; j < getGameIndicator(); j++) { //check for any correct letter (yellow)
            for (int k = 0; k < getGameIndicator(); k++) {
                if (inputWord[j] == answerTemp[k] && colorForLetter[j] != 2) {
                    //if that letter is not already green and matches some other letter
                    colorForLetter[j] = 1;
                    answerTemp[k] = '-';
                }
            }
        }

        for (int m = 0; m < getGameIndicator(); m++) {
            if (colorForLetter[m] == 0) {
                allLabels[getGuessNumber()][m].setBackground(new Color(120, 124, 127));
            }
            if (colorForLetter[m] == 1) {
                allLabels[getGuessNumber()][m].setBackground(new Color(200, 182, 83));
            }
            if (colorForLetter[m] == 2) {
                allLabels[getGuessNumber()][m].setBackground(new Color(108, 169, 101));
            }
        }

        return correct;
    }

