    public void guessEntered() {
        cheatLabel.setVisible(false);
        guess = guessField.getText(); // gets guess value from field

        if (guess.length() != getGameIndicator()) {
            guessPrompt.setText("Enter a " + getGameIndicator() + " letter word.");
        } else if (getGuessNumber() >= 6 || guess == getCorrectAnswer()) {
        } else {
            for (int i = 0; i < gameIndicator; i++) {
                input[i] = guess.toUpperCase().charAt(i);
            }// assigns guess to []

            for (int l = 0; l < getGameIndicator(); l++) {
                allLabels[getGuessNumber()][l].setText(Character.toString(input[l]));
            }
            if (DisplayColorLetters(input, answer)) {
                guessPrompt.setText("Congratulations, you win!");
                guessButton.setText("You found the correct word =>");
                viewPlayerData.setText("View your data!");
                playerDataTextBox.setVisible(true);
                cheatLabel.setVisible(true);
                winIndicator = 1;
                UpdatePlayerData();
                getPlayerDataString();
                playerDataTextBox.setText(playerDataString);
            } else if (guessNumber > 4) {

                guessPrompt.setText("Sorry, try again next time.");
                guessButton.setText("Here's the correct word =>");
                viewPlayerData.setText("View your data!");
                playerDataTextBox.setVisible(true);
                cheatLabel.setVisible(true);
                winIndicator = 0;
                UpdatePlayerData();
                getPlayerDataString();
                playerDataTextBox.setText(playerDataString);
            }
            guessNumber = guessNumber + 1;
        }
        guessField.setText("");
    }

